#Question 6 - Programme qui trie un tableau par ordre croissant

#Indique le rôle du programme à l'utilisateur
print("Question 6 - Programme qui trie un tableau par ordre croissant")

#Initialisation du tableau avec les valeurs de l'énoncé
tableau = [7, 5, 4, 9, 3]

#Affichage du tableau non trié
print(f"Le tableau non trié est : {tableau}")

#Utilisation de la méthode sort pour trier le tableau par ordre croissant
tableau.sort()

#Affichage du tableau
print(f"Le tableau trié par ordre croissant est : {tableau}")
